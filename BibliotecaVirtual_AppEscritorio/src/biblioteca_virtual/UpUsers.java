    private void buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonMousePressed
        if(name.getText().equals("Ingrese el nombre") || ap1.getText().equals("Ingrese el apellido")
            || ap2.getText().equals("Ingrese el email") || pass.getPassword().equals("********")
            
            ){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            name.requestFocus();
        }
        else
        {
            String uname = name.getText();
            String uapp = ap1.getText();
            String uapm = ap2.getText();
            String password;
            password = String.valueOf(pass.getPassword());
    
            
   

            if(uname == null || "".equals(uname) || uapp == null || "".equals(uapp) || uapm == null || "".equals(uapm) 
             || password == null || "".equals(password) // || "".equals(udom) || utel == null || "".equals(utel)
                    
                    ){
                javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                name.requestFocus();
            }
            else{ 
                try {
                    if(edit)
                //        EditUser(idus, uname, uapp, uapm, udom, utel);
                        EditUser(idus, uname, uapp, uapm, password);
                    else
      
                        InsertUser(uname, uapp, uapm, password);

                    name.setText("");
                    ap1.setText("");
                    ap2.setText("");
                    pass.setText("");
    

                    if(edit){
                        Users p1 = new Users();
                        p1.setSize(750, 430);
                        p1.setLocation(0, 0);

                        Login.removeAll();
                        Login.add(p1, BorderLayout.CENTER);
                        Login.revalidate();
                        Login.repaint();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(UpUsers.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                
            }
        }
    }//GEN-LAST:event_buttonMousePressed


     public void InsertUser(String name, String app, String apm, String password) throws SQLException{
        Statement stm = reg.createStatement();
        
    
        stm.executeUpdate("INSERT INTO `users` (`name`, `last_name_p`, `email`, `pass` ) VALUES ('"+name+"','"+app+"','"+apm+"','"+password+"')");
       // stm.executeUpdate("INSERT INTO `users` (`name`, `last_name_p`, `email`, `pass` ) VALUES ('luciano','gaitan','lucianogaitan','123456')");
        javax.swing.JOptionPane.showMessageDialog(this, "¡Usuario registrado correctamente! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
    }
    

       public void EditUser(String id, String name, String app, String apm, String pass) throws SQLException{
        Statement stm = reg.createStatement();
        
        stm.executeUpdate("UPDATE `users` SET `name` = '"+name+"', `last_name_p` = '"+app+"', `pass` = '"+pass+"', `email` = '"+apm+"' WHERE `id` = "+ id +";");
        javax.swing.JOptionPane.showMessageDialog(this, "¡Usuario editado correctamente! \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
    }

